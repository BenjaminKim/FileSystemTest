void ApiFunction::SetLastError()
{
    DWORD error = GetLastError();

    std::wostringstream oss;

    oss << error << '(' << GetErrorDefineString(error) << ')';

    lastError_ = oss.str();
}

void ApiFunction::Output(std::wostream& output) const
{
    if(!enableLog_)
    {
        return;
    }

    output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << apiFunctionName_ << L" IN" << std::endl;

    const std::vector<param>::const_iterator itInEnd = argInList_.end();
    size_t maxLength = 0;
    for(std::vector<param>::const_iterator it = argInList_.begin(); it < itInEnd; ++it)
    {
        const param& p = *it;
        if(maxLength < p.first.length())
        {
            maxLength = p.first.length();
        }
    }
    maxLength += 2;
    for(std::vector<param>::const_iterator it = argInList_.begin(); it < itInEnd; ++it)
    {
        const param& p = *it;
        
        output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << setw(maxLength) << p.first << L" = " << p.second << std::endl;
    }

    output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << apiFunctionName_ << L" OUT" << std::endl;

    const std::vector<param>::const_iterator itOutEnd = argOutList_.end();
    maxLength = 0;
    for(std::vector<param>::const_iterator it = argOutList_.begin(); it < itOutEnd; ++it)
    {
        const param& p = *it;
        if(maxLength < p.first.length())
        {
            maxLength = p.first.length();
        }
    }
    maxLength += 2;
    for(std::vector<param>::const_iterator it = argOutList_.begin(); it < itOutEnd; ++it)
    {
        const param& p = *it;
        output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << setw(maxLength) << p.first << L" = " << p.second << std::endl;
    }

    output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << L"return: " << returnValue_ << std::endl;
    output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << L"last error: " << lastError_ << std::endl;

    if(printDuration_)
    {
        output << (printDateTime_ ? Timer::GetCurrentDateTime() : L"") << L"Duration: " << elapsedtime_ << L" second." << std::endl;
    }
}

void ApiFunction::OutputFlush()
{
    if(enableLog_)
    {
        output_ << *this;
        argInList_.clear();
        argOutList_.clear();
    }
}

void ApiFunction::ResetStatistics()
{
    statistics_.clear();
}

void ApiFunction::PrintStatistics() const
{
    if(this->statistics_.Count() == 0)
    {
        return;
    }
    
    output_.precision(12);
    output_ << L"Function Name : " << apiFunctionName_ << endl;
    output_ << L"Min : " << fixed << statistics_.Min() << endl;
    output_ << L"Max : " << fixed << statistics_.Max() << endl;
    output_ << L"Sum : " << fixed << statistics_.Sum() << endl;
    output_ << L"Count : " << fixed << statistics_.Count() << endl;
    output_ << L"Operation per sec : " << fixed << statistics_.Count() / statistics_.Sum() << endl;
    output_ << L"Avg : " << fixed << statistics_.Avg() << endl;
    output_ << L"StdDev : " << fixed << statistics_.StdDev() << endl << endl;
}

