wstring GetStringHANDLE(HANDLE h)
{
    if(h == INVALID_HANDLE_VALUE)
    {
        return L"(INVALID_HANDLE)";
    }
    else if(h == 0)
    {
        return L"(NULL)";
    }
    else
    {
        return L"(VALID_HANDLE)";
    }
}

wstring GetStringHandleValue(HANDLE h)
{
    wostringstream oss;

    oss << hexf << (unsigned int)h;

    if(h == INVALID_HANDLE_VALUE)
    {
        oss << L"(INVALID_HANDLE_VALUE)";
    }
    else if(h == 0)
    {
        oss << L"(NULL)";
    }

    return oss.str();
}

wstring GetStringPVOID(const void* p)
{
    return (p) ? L"(NOT NULL)" : L"(NULL)";
}

wstring GetStringPointerValue(const void* p)
{
    if(p)
    {
        wostringstream oss;

        oss << hexf << (unsigned int)p;
        return oss.str();
    }

    return L"(NULL)";
}

wstring GetStringBOOL(BOOL b)
{
    wostringstream oss;
    if(b == FALSE)
    {
        oss << (int)b << L"(FALSE)";
    }
    else if(b == TRUE)
    {
        oss << (int)b << L"(TRUE)";
    }
    else
    {
        oss << (int)b << L"(unknown)";
    }
    return oss.str();
}

wstring GetStringPBOOL(const BOOL* p)
{
    if(!p)
    {
        return L"(NULL)";
    }

    return GetStringBOOL(*p);
}

